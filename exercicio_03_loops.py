# Exercício 03 - Laços de repetição
# Utilizando for e while

# Exemplo com for
for numero in range(1, 6):
    print("Número:", numero)


# Exemplo com while
contador = 1

while contador <= 5:
    print("Contador:", contador)
    contador += 1


# Percorrendo uma lista
nomes = ["Ana", "Bruno", "Carlos"]

for nome in nomes:
    print("Nome:", nome)
